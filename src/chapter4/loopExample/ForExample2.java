package chapter4.loopExample;

public class ForExample2 {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            System.out.println("hello, world");
        }

        System.out.println();

        for (var i = 1; i <= 10; i++) {
            System.out.println("hello, world");
        }
    }

}
